// Helper to get location by slug
export function getLocationBySlug(slug: string): Location | undefined {
  return locations.find((loc) => loc.slug === slug);
}

// Helper to get nearby locations as full objects
export function getNearbyLocations(location: Location): Location[] {
  return location.nearbyAreas
    .map((slug) => getLocationBySlug(slug))
    .filter((loc): loc is Location => loc !== undefined);
}

// Get all location slugs for static generation
export function getAllLocationSlugs(): string[] {
  return locations.map((loc) => loc.slug);
}
